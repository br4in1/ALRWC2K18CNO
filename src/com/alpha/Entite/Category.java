package com.alpha.Entite;

/**
 *
 * @author br4in
 */
public class Category {
	int id;
	String label;

	public Category(int id, String label) {
		this.id = id;
		this.label = label;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
}
